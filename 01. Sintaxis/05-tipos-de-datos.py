# Tipos de datos de variables:
nada = None
cadena = "Programación Ucatec"
entero = 7
decimal = 10.50
logico = False
listaNumeros = [10, 20, 30, 40, 50, 60]
listaNombres = ["Juan", "Carlos", "María", "Fernanda"]
# La tupla  es similar a una lista pero no cambia
tupla = ("Programación", "en", "Python")
# Un diccionario es una lista con clave y valor.
diccionario = {
    "nombre": "Juan",
    "apellido": "Perez",
    "ocupacion": "Programador"
}
rango = range(9)
dato_byte = b"Ucatec"

# Imprimiendo datos de variables:
print(nada)
print(cadena)
print(entero)
print(decimal)
print(logico)
print(listaNumeros)
print(listaNombres)
print(tupla)
print(diccionario)
print(rango)
print(dato_byte)

# Mostrando tipos de datos:
print(type(nada))

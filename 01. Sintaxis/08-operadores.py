# Operadores de asignación:
a = 2
b = 2

# Operadores aritméticos:
print("Operaciones Aritméticas")
print(f"La suma es: {a + b}")
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

